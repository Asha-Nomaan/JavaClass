package com.class11;

public class Cars {

	public static void main(String[] args) {

		String[] cars= {"Dodge", "BMW", "Audi", "Jeep", "Toyota", "Ford"};
		for(String made:cars) {
			System.out.println(made);
		}
		for (int i = 0; i <= cars.length - 1; i++) {
			System.out.println(cars[i]);
	}
	}
}


